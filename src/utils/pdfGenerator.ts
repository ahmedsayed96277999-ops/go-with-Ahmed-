  doc.setFillColor(navyDark.r, navyDark.g, navyDark.b);
  doc.rect(15, 15, 180, 24, "F");

  // Logo & System header
  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(16);
  doc.text("GO WITH AHMED TRAVELS", 22, 25);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(7.5);
  doc.setTextColor(200, 220, 240);
  doc.text("GLOBAL TRAVEL ARCHITECTS & FLIGHT COORDINATORS", 22, 30);

  // E-Ticket Right aligned info
  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.text("ELECTRONIC E-TICKET RECEIPT", 125, 24);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8.5);
