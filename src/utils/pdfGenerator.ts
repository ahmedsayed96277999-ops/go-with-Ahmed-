// Helper to draw a dashed line
function drawDashedLine(doc: jsPDF, x1: number, y1: number, x2: number, y2: number, dashLength = 2, gapLength = 2) {
  const dx = x2 - x1;
  const dy = y2 - y1;
  const distance = Math.sqrt(dx * dx + dy * dy);
  const numDashes = Math.floor(distance / (dashLength + gapLength));
  const xStep = dx / numDashes;
  const yStep = dy / numDashes;

  for (let i = 0; i < numDashes; i++) {
    const startX = x1 + i * xStep;
    const startY = y1 + i * yStep;
    const endX = startX + (dashLength / (dashLength + gapLength)) * xStep;
    const endY = startY + (dashLength / (dashLength + gapLength)) * yStep;
    doc.line(startX, startY, endX, endY);
  }
}

